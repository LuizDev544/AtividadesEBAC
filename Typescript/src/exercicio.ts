// Função de multiplicação
function multiplicar(a: number, b: number): number {
    return a * b;
}

// Função de saudação
function saudacao(nome: string): string {
    return "Olá " + nome;
}

// Exemplos de uso:
console.log(multiplicar(5, 3)); // Saída: 15
console.log(saudacao("Luiz"));  // Saída: Olá Luiz